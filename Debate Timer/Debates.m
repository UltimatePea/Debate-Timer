#import "Debates.h"

@implementation Debates

- (NSMutableArray<Debate *> *)debates
{
    if (!_debates) {
        _debates = [[NSMutableArray alloc] init];
    }
    return _debates;
}
#define KEY_DEBATES @"KEY_DEBATES"

- (void)encodeWithCoder:(nonnull NSCoder *)aCoder
{
    [aCoder encodeObject:self.debates forKey:KEY_DEBATES];
}

- (instancetype)initWithCoder:(nonnull NSCoder *)aDecoder
{
    self = [super init];
    if (self) {
        self.debates = [aDecoder decodeObjectForKey:KEY_DEBATES];
    }
    return self;
}


@end
